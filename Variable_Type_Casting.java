
public class Variable_Type_Casting {

	public static void main(String[] args) {
		// Variable and type casting in java 
		int a=20;
	    double s=a;
	    double m=9.7767567586565465466857644;
	    short n=(short) m;
	    float q=(float) m;
	    System.out.println("a=" +a);
	    System.out.println("double=" +s);
	    System.out.println("m=" +m);
	    System.out.println("short=" +n);
	    System.out.println("float="  +q);

	}

}
